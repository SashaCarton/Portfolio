          // Masquer tous les paragraphes
          for (let i = 0; i < paragraphs.length; i++) {
            paragraphs[i].style.display = 'none';
          }
          // Afficher le paragraphe suivant
          paragraphs[index].style.display = 'block';
          // Passer au paragraphe suivant
