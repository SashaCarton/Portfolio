        window.addEventListener('scroll', function() {
          var scrollPosition = window.scrollX || window.pageXOffset || document.documentElement.scrollLeft;
          var windowHeight = window.innerHeight;
          var documentHeight = document.documentElement.scrollHeight;
          var halfDocumentHeight = documentHeight / 2;

          if (scrollPosition > halfDocumentHeight - windowHeight) {
            document.body.style.backgroundColor = 'blue'; // Change the background color to blue
          } else {
            document.body.style.backgroundColor = 'white'; // Change the background color back to white
          }
        });

